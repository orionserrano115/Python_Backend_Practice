#operadores logicos and, or o not
#explicación
'''
and = dos condiciones deben ser reales para true todo los demas dara false
or =  una de dos condiciones debe ser real para true si ninguna es real dara false
not = invierte el valor, si la operacion es true y colocas not dara false
'''
a = 5
b = 10

print((a<b) and (b > a))
print((a<b) or (b<a))
